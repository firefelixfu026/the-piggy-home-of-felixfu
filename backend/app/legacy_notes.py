             '## 3. HTTP 协议详解\n'
             '\n'
             '### 3.1 协议概述\n'
             '\n'
             'HTTP（HyperText Transfer Protocol，超文本传输协议）是 Web '
             '中最核心的应用层协议之一。它采用经典的**请求-响应**模型：客户端发送请求，服务器处理请求并返回响应。\n'
             '\n'
             '典型流程：\n'
             '\n'
             '```text\n'
             '客户端浏览器\n'
             '  -> 发送 HTTP 请求\n'
             '  -> Web 服务器处理请求\n'
             '  -> 返回 HTTP 响应\n'
             '  -> 浏览器解析并渲染结果\n'
             '```\n'
             '\n'
             '前后端必须基于统一通信协议进行数据交互，原因在于：\n'
             '\n'
             '- **请求格式统一**：前端知道如何表达资源路径、请求方法、请求头和请求体。\n'
             '- **响应格式统一**：后端按约定返回状态码、响应头和响应体。\n'
             '- **语义明确**：GET、POST、PUT、DELETE 等方法表达不同操作意图。\n'
             '- **跨语言协作**：前端可以使用 JavaScript，后端可以使用 Java、Python、Go、Node.js，只要遵循 HTTP 规范即可通信。\n'
             '- **基础设施兼容**：浏览器、代理、网关、CDN、负载均衡器都能识别 HTTP。\n'
             '\n'
             'HTTP 本身是无状态协议。服务器默认不会记住两次请求之间的关系，因此 Web 应用通常借助 Cookie、Session、Token '
             '等机制维护登录状态和用户上下文。\n'
             '\n'
             '### 3.2 HTTP 请求与响应结构\n'
             '\n'
             '**请求结构**：HTTP 请求由请求行、请求头、空行和请求体组成。\n'
             '\n'
             '```text\n'
             'GET /users?id=1 HTTP/1.1\n'
             'Host: example.com\n'
             'User-Agent: Mozilla/5.0\n'
             'Accept: application/json\n'
             '\n'
             '```\n'
             '\n'
             '主要组成部分：\n'
             '\n'
             '- **请求方法**\n'
             '\n'
             '  请求方法用于表达客户端希望对资源执行的操作。\n'
             '\n'
             '  | 方法 | 主要用途 | 常见场景 |\n'
             '  | --- | --- | --- |\n'
             '  | GET | 获取资源 | 打开页面、查询列表、获取详情。 |\n'
             '  | POST | 提交数据或创建资源 | 登录、注册、提交表单、创建订单。 |\n'
             '  | PUT | 整体更新资源 | 替换用户资料、更新完整配置。 |\n'
             '  | PATCH | 局部更新资源 | 修改昵称、更新部分字段。 |\n'
             '  | DELETE | 删除资源 | 删除文章、移除记录。 |\n'
             '\n'
             '- **请求 URL**\n'
             '\n'
             '  URL 用于定位资源，通常包含协议、主机名、端口、路径和查询参数。\n'
             '\n'
             '  ```text\n'
             '  https://api.example.com:443/users?id=1\n'
             '  ```\n'
             '\n'
             '  拆解如下：\n'
             '\n'
             '  - `https`：协议。\n'
             '  - `api.example.com`：主机名。\n'
             '  - `443`：端口号。\n'
             '  - `/users`：资源路径。\n'
             '  - `id=1`：查询参数。\n'
             '\n'
             '- **请求头 (Headers)**\n'
             '\n'
             '  请求头用于携带请求的元数据，例如客户端类型、内容格式、身份凭证、缓存策略。\n'
             '\n'
             '  常见请求头：\n'
             '\n'
             '  | 请求头 | 作用 |\n'
             '  | --- | --- |\n'
             '  | Host | 指定目标主机名。 |\n'
             '  | User-Agent | 描述客户端类型和版本。 |\n'
             '  | Accept | 声明客户端可接收的响应类型。 |\n'
             '  | Content-Type | 声明请求体的数据格式。 |\n'
             '  | Authorization | 携带身份认证信息，例如 Bearer Token。 |\n'
             '  | Cookie | 携带浏览器保存的站点状态。 |\n'
             '\n'
             '- **请求体 (Body)**\n'
             '\n'
             '  请求体用于承载提交给服务器的数据。GET 请求通常没有请求体，POST、PUT、PATCH 常携带请求体。\n'
             '\n'
             '  JSON 请求体示例：\n'
             '\n'
             '  ```json\n'
             '  {\n'
             '    "username": "alice",\n'
             '    "password": "example-password"\n'
             '  }\n'
             '  ```\n'
             '\n'
             '**响应结构**：HTTP 响应由状态行、响应头、空行和响应体组成。\n'
             '\n'
             '```text\n'
             'HTTP/1.1 200 OK\n'
             'Content-Type: application/json\n'
             'Cache-Control: no-cache\n'
             '\n'
             '{"id":1,"name":"alice"}\n'
             '```\n'
             '\n'
             '主要组成部分：\n'
             '\n'
             '- **状态码**\n'
             '\n'
             '  状态码用于说明服务器对请求的处理结果。它让客户端能够快速判断请求成功、重定向、认证失败、资源不存在或服务器异常。\n'
             '\n'
             '- **响应头 (Headers)**\n'
             '\n'
             '  响应头用于描述响应元数据，例如内容类型、缓存策略、服务器信息、Cookie 设置、跨域策略。\n'
             '\n'
             '  常见响应头：\n'
             '\n'
             '  | 响应头 | 作用 |\n'
             '  | --- | --- |\n'
             '  | Content-Type | 声明响应体格式，例如 `text/html`、`application/json`。 |\n'
             '  | Content-Length | 声明响应体长度。 |\n'
             '  | Cache-Control | 控制缓存策略。 |\n'
             '  | Set-Cookie | 要求浏览器保存 Cookie。 |\n'
             '  | Location | 配合重定向状态码，指示新的访问地址。 |\n'
             '  | Access-Control-Allow-Origin | 控制跨域资源访问。 |\n'
             '\n'
             '- **响应体 (Body)**\n'
             '\n'
             '  响应体是服务器返回的实际内容，可以是 HTML 页面、JSON 数据、图片、文件流或错误信息。\n'
             '\n'
             '**核心状态码速查**：\n'
             '\n'
