             '**模型概述**：TCP/IP '
             '网络分层模型用于把复杂的网络通信拆解为多个职责清晰的层级。每一层只关注本层需要解决的问题，并向上层提供稳定服务。分层的意义在于降低系统复杂度：应用程序不需要直接处理电信号，网络设备也不需要理解网页、邮件或文件传输的业务语义。\n'
             '\n'
             '经典 TCP/IP 模型通常划分为四层：\n'
             '\n'
