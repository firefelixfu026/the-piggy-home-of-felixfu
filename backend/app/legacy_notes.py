             '### 2.2 DNS 与域名解析\n'
             '\n'
             '**DNS 的作用**：DNS（Domain Name System，域名系统）用于将人类可读的域名转换为计算机可定位的 IP 地址。用户更容易记住 '
             '`www.example.com`，但计算机通信最终需要使用 IP 地址定位目标主机。\n'
             '\n'
             'DNS 的核心价值：\n'
             '\n'
             '- 用域名屏蔽底层 IP 地址变化。\n'
             '- 通过层级结构管理全球域名。\n'
             '- 支持负载均衡、故障切换和地理位置调度。\n'
             '- 降低用户记忆成本，提高服务迁移灵活性。\n'
             '\n'
             '**解析流程梳理**：\n'
             '\n'
             '1. **浏览器接收域名**\n'
             '\n'
             '   用户在浏览器输入 URL，例如：\n'
             '\n'
             '   ```text\n'
             '   https://www.example.com/index.html\n'
             '   ```\n'
             '\n'
             '   浏览器首先提取主机名 `www.example.com`，后续需要将其解析为 IP 地址。\n'
             '\n'
             '2. **查询浏览器 DNS 缓存**\n'
             '\n'
             '   浏览器会优先检查自身缓存。如果缓存中存在未过期的解析结果，则直接使用该 IP 地址。\n'
             '\n'
             '3. **查询操作系统 DNS 缓存**\n'
             '\n'
             '   如果浏览器缓存未命中，操作系统会检查本地 DNS 缓存，常见系统会缓存近期解析过的域名结果。\n'
             '\n'
             '4. **检查本地 hosts 文件**\n'
             '\n'
             '   操作系统可能会读取 hosts 文件。hosts 文件提供静态域名到 IP 的映射，优先级通常高于外部 DNS 查询。\n'
             '\n'
             '5. **向本地 DNS 服务器发起请求**\n'
             '\n'
             '   如果本地仍未命中，系统会向配置的本地 DNS 服务器发起查询。本地 DNS 服务器通常由路由器、运营商或公共 DNS 服务提供。\n'
             '\n'
             '6. **递归或迭代查询权威 DNS**\n'
             '\n'
             '   本地 DNS 服务器如果没有缓存，会继续查询根域名服务器、顶级域名服务器和权威域名服务器，逐步定位最终记录。\n'
             '\n'
             '7. **返回解析结果**\n'
             '\n'
             '   DNS 服务器将 IP 地址返回给操作系统，操作系统再交给浏览器。\n'
             '\n'
             '8. **建立连接并缓存结果**\n'
             '\n'
             '   浏览器使用解析得到的 IP 地址建立 TCP 或 TLS 连接，并按 DNS 记录的 TTL 缓存解析结果。\n'
             '\n'
