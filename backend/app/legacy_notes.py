             '| 原则 | 说明 |\n'
             '| --- | --- |\n'
             '| 同级唯一 | `key` 只需要在当前列表的兄弟节点之间唯一 |\n'
             '| 稳定不变 | 数据顺序变化后，同一条数据仍应拥有相同 `key` |\n'
             '| 优先使用业务 ID | 数据库 ID、UUID、稳定编码通常优于数组下标 |\n'
             '| 避免随机值 | `Math.random()` 会导致每次渲染都被视为新元素 |\n'
             '| 谨慎使用下标 | 只适合静态列表；可排序、可插入、可删除列表不应使用下标 |\n'
             '\n'
             '错误示例：\n'
             '\n'
             '```jsx\n'
             'items.map((item, index) => (\n'
             '  <TodoItem key={index} item={item} />\n'
             '));\n'
             '```\n'
             '\n'
             '当列表插入、删除或排序时，数组下标会变化，React 可能错误复用旧组件实例，导致输入框内容、动画状态、焦点状态等与数据不匹配。\n'
             '\n'
             '## 3. 工程化开发环境搭建\n'
             '\n'
             '**脚手架对比**：\n'
             '\n'
             '| 脚手架名称 | 构建命令 | 核心定位/优势 |\n'
             '| --- | --- | --- |\n'
             '| Vite | `npm create vite@latest my-app -- --template react-ts` | '
             '面向现代前端的轻量级构建工具，开发服务器启动快，HMR 性能好，适合 SPA、组件库、后台系统等客户端应用 |\n'
             '| Next.js | `npx create-next-app@latest my-app` | React 全栈框架，支持 SSR、SSG、ISR、API '
             'Routes、文件路由与 App Router，适合内容站点、SEO 场景、服务端渲染与全栈应用 |\n'
             '\n'
             '选择建议如下：\n'
             '\n'
             '| 场景 | 更适合的方案 | 原因 |\n'
             '| --- | --- | --- |\n'
             '| 纯客户端 SPA | Vite | 配置轻、启动快、构建链路直接 |\n'
             '| 管理后台 | Vite | 通常不依赖 SEO，重交互和权限路由更常见 |\n'
             '| 内容站点或营销页 | Next.js | SSR/SSG 更利于首屏性能与搜索引擎抓取 |\n'
             '| 全栈 React 应用 | Next.js | 路由、服务端组件、接口层与部署模型更完整 |\n'
             '| 组件库开发 | Vite | 库模式、开发体验与构建速度更适合轻量包开发 |\n'
             '\n'
             '**核心文件渲染链路**：以 Vite React 项目为例，典型入口链路为 `index.html -> main.tsx -> App.tsx`。\n'
             '\n'
             '| 文件 | 作用 | 关键职责 |\n'
             '| --- | --- | --- |\n'
             '| `index.html` | 浏览器入口 HTML | 提供根 DOM 节点，加载模块脚本 |\n'
             '| `src/main.tsx` | React 应用入口 | 获取根节点，创建 React Root，渲染根组件 |\n'
             '| `src/App.tsx` | 根组件 | 组织页面结构、路由、布局与顶层状态 |\n'
             '\n'
             '典型结构如下：\n'
             '\n'
             '```html\n'
             '<!-- index.html -->\n'
             '<!doctype html>\n'
             '<html lang="zh-CN">\n'
             '  <head>\n'
             '    <meta charset="UTF-8" />\n'
             '    <meta name="viewport" content="width=device-width, initial-scale=1.0" />\n'
             '    <title>React App</title>\n'
             '  </head>\n'
             '  <body>\n'
             '    <div id="root"></div>\n'
             '    <script type="module" src="/src/main.tsx"></script>\n'
             '  </body>\n'
             '</html>\n'
             '```\n'
             '\n'
             '```tsx\n'
             '// src/main.tsx\n'
             'import { StrictMode } from "react";\n'
             'import { createRoot } from "react-dom/client";\n'
             'import App from "./App";\n'
             '\n'
             'createRoot(document.getElementById("root")!).render(\n'
             '  <StrictMode>\n'
             '    <App />\n'
             '  </StrictMode>,\n'
             ');\n'
             '```\n'
             '\n'
             '```tsx\n'
             '// src/App.tsx\n'
             'export default function App() {\n'
             '  return <main>React Application</main>;\n'
             '}\n'
             '```\n'
             '\n'
             '`createRoot().render()` 的作用是创建 React 18 的并发根节点，并将 React 组件树挂载到真实 DOM '
             '容器中。`createRoot` 替代旧版 `ReactDOM.render`，使应用具备 React 18 的自动批处理、并发渲染能力基础。\n'
             '\n'
             '`<StrictMode>` 是开发环境辅助组件，不会渲染真实 DOM。它用于暴露潜在副作用问题、废弃 API 使用、非纯渲染逻辑等风险。在 React 18 '
             '开发环境中，严格模式可能会对部分生命周期或 Effect 执行额外检查，因此副作用逻辑必须具备幂等性与正确清理能力。\n'
             '\n'
             '## 4. Hooks 核心机制深度剖析\n'
             '\n'
             '**函数式编程理念**：纯函数是指在相同输入下始终返回相同输出，并且执行过程中不修改外部状态、不产生不可控副作用的函数。React '
             '函数组件在理想模型中接近纯函数：输入为 Props 与 State，输出为 UI 描述。\n'
             '\n'
             '函数组件的直接问题是：普通局部变量无法跨渲染保存状态，数据请求、订阅、定时器、手动 DOM 操作等副作用也不能直接写入渲染过程。Hooks 提供了在函数组件中接入 '
             'React 状态系统与副作用生命周期的能力。\n'
             '\n'
             'Hooks 的基本约束如下：\n'
             '\n'
             '| 规则 | 说明 |\n'
             '| --- | --- |\n'
             '| 只在函数组件或自定义 Hook 中调用 | 保证 Hook 与 React 渲染上下文绑定 |\n'
             '| 只在顶层调用 | 不应写在条件、循环、嵌套函数中，保证每次渲染调用顺序一致 |\n'
             '| Hook 名称以 `use` 开头 | 便于静态检查与语义识别 |\n'
             '| 渲染逻辑保持纯净 | 数据请求、订阅、定时器等副作用应进入 `useEffect` 等 Hook |\n'
             '\n'
             '### 4.1 useState (状态管理)\n'
             '\n'
             '**机制解析**：`useState` 用于在函数组件中声明状态。它接收初始值，返回一个数组：第一项为当前状态值，第二项为状态更新函数。\n'
             '\n'
             '```tsx\n'
             'import { useState } from "react";\n'
             '\n'
             'function Counter() {\n'
             '  const [count, setCount] = useState(0);\n'
             '\n'
             '  return (\n'
             '    <button onClick={() => setCount(count + 1)}>\n'
             '      Count: {count}\n'
             '    </button>\n'
             '  );\n'
             '}\n'
             '```\n'
             '\n'
             '数组结构通常通过解构赋值使用：\n'
             '\n'
             '| 返回项 | 示例 | 说明 |\n'
             '| --- | --- | --- |\n'
             '| 当前状态 | `count` | 当前渲染快照中的状态值 |\n'
             '| 更新函数 | `setCount` | 通知 React 安排下一次状态更新与重新渲染 |\n'
             '\n'
             '初始值只在组件首次渲染时使用。昂贵的初始计算应传入函数，避免每次渲染重复执行。\n'
             '\n'
             '```tsx\n'
             'const [items, setItems] = useState(() => loadInitialItems());\n'
             '```\n'
             '\n'
             '**避坑指南**：React 状态更新依赖引用变化判断。直接修改对象或数组的内存地址不会形成新的引用，可能导致 React 无法正确感知变化，也会破坏不可变数据约束。\n'
             '\n'
             '错误示例：\n'
             '\n'
             '```tsx\n'
             'const [user, setUser] = useState({ name: "Ada", age: 28 });\n'
             '\n'
             'function updateAge() {\n'
             '  user.age = 29;\n'
             '  setUser(user);\n'
             '}\n'
             '```\n'
             '\n'
             '正确示例：\n'
             '\n'
             '```tsx\n'
             'const [user, setUser] = useState({ name: "Ada", age: 28 });\n'
             '\n'
             'function updateAge() {\n'
             '  setUser({\n'
             '    ...user,\n'
             '    age: 29,\n'
             '  });\n'
             '}\n'
             '```\n'
             '\n'
             '对象与数组更新规则如下：\n'
             '\n'
             '| 数据结构 | 错误方式 | 推荐方式 |\n'
             '| --- | --- | --- |\n'
             '| 对象 | `user.name = "Grace"` | `setUser({ ...user, name: "Grace" })` |\n'
             '| 数组追加 | `list.push(item)` | `setList([...list, item])` |\n'
             '| 数组删除 | `list.splice(index, 1)` | `setList(list.filter((item) => item.id !== id))` '
             '|\n'
             '| 数组替换 | `list[index] = next` | `setList(list.map((item) => item.id === id ? next : '
             'item))` |\n'
             '\n'
             '`useState` 的更新函数默认执行替换操作，不会像 class component 的 `setState` 一样自动浅合并对象。\n'
             '\n'
             '```tsx\n'
             'const [form, setForm] = useState({\n'
             '  email: "",\n'
             '  password: "",\n'
             '});\n'
             '\n'
             'function updateEmail(email: string) {\n'
             '  setForm({\n'
             '    ...form,\n'
             '    email,\n'
             '  });\n'
             '}\n'
             '```\n'
             '\n'
             'React 18 默认启用 Automatic Batching（自动批处理）。在同一个事件循环任务中，多次状态更新会被合并为一次渲染提交，从而减少重复渲染。\n'
             '\n'
             '```tsx\n'
             'function handleClick() {\n'
             '  setCount((count) => count + 1);\n'
             '  setVisible(true);\n'
             '  setStatus("done");\n'
             '}\n'
             '```\n'
             '\n'
             '当下一次状态依赖上一次状态时，应使用函数式更新，避免读取当前渲染快照中的旧值。\n'
             '\n'
             '```tsx\n'
             'setCount((current) => current + 1);\n'
             '```\n'
             '\n'
             '### 4.2 useEffect (副作用处理)\n'
             '\n'
             '**参数解构**：`useEffect` 用于处理渲染完成后的副作用。典型副作用包括网络请求、事件订阅、定时器、日志上报、手动操作 DOM、与非 React '
             '系统同步状态等。\n'
             '\n'
             '基本语法如下：\n'
             '\n'
             '```tsx\n'
             'useEffect(() => {\n'
             '  // effect: 执行副作用\n'
             '\n'
             '  return () => {\n'
             '    // cleanup: 清理副作用\n'
             '  };\n'
             '}, [deps]);\n'
             '```\n'
             '\n'
             '`useEffect` 的核心参数含义如下：\n'
             '\n'
             '| 组成部分 | 说明 | 典型用途 |\n'
             '| --- | --- | --- |\n'
             '| `effect` 回调函数 | 浏览器完成渲染提交后执行的副作用逻辑 | 发起请求、订阅事件、启动定时器 |\n'
             '| `cleanup` 清除函数 | 下一次 effect 执行前或组件卸载时执行 | 取消订阅、清除定时器、中止请求、释放资源 |\n'
             '| `deps` 依赖数组 | 声明 effect 使用到的响应式值 | 控制副作用何时重新执行 |\n'
             '\n'
             '依赖数组行为如下：\n'
             '\n'
             '| 写法 | 执行时机 | 适用场景 |\n'
             '| --- | --- | --- |\n'
             '| 不传依赖数组 | 每次渲染后都执行 | 极少使用，容易造成重复副作用 |\n'
             '| `[]` | 组件挂载后执行一次，卸载时清理 | 初始化订阅、首屏请求、第三方实例创建 |\n'
             '| `[a, b]` | 首次渲染后执行，且 `a` 或 `b` 变化后重新执行 | 与特定状态、Props、派生参数同步 |\n'
             '\n'
             '```tsx\n'
             'useEffect(() => {\n'
             '  const controller = new AbortController();\n'
             '\n'
             '  fetch(`/api/users/${userId}`, {\n'
             '    signal: controller.signal,\n'
             '  });\n'
             '\n'
             '  return () => {\n'
             '    controller.abort();\n'
             '  };\n'
             '}, [userId]);\n'
             '```\n'
             '\n'
             '**闭包陷阱与状态快照**：函数组件每次渲染都会创建新的函数作用域。该次渲染中的 Props、State、事件处理函数与 Effect '
             '回调共同构成一个“状态快照”。异步回调、定时器或事件监听器如果引用了某次渲染中的 state，就会持续读取该次快照中的旧值。\n'
             '\n'
             '典型问题如下：\n'
             '\n'
             '```tsx\n'
             'function Counter() {\n'
             '  const [count, setCount] = useState(0);\n'
             '\n'
             '  useEffect(() => {\n'
             '    const timer = setInterval(() => {\n'
             '      setCount(count + 1);\n'
             '    }, 1000);\n'
             '\n'
             '    return () => clearInterval(timer);\n'
             '  }, []);\n'
             '\n'
             '  return <p>{count}</p>;\n'
             '}\n'
             '```\n'
             '\n'
             '该示例中，`deps` 为空数组，Effect 只在首次渲染时执行。定时器回调捕获的是首次渲染的 `count`，因此后续回调仍基于旧快照计算。\n'
             '\n'
             '解决方案一：使用函数式更新读取最新状态。\n'
             '\n'
             '```tsx\n'
             'useEffect(() => {\n'
             '  const timer = setInterval(() => {\n'
             '    setCount((current) => current + 1);\n'
             '  }, 1000);\n'
             '\n'
             '  return () => clearInterval(timer);\n'
             '}, []);\n'
             '```\n'
             '\n'
             '解决方案二：完整声明依赖，使 Effect 随状态变化重新同步。\n'
             '\n'
             '```tsx\n'
             'useEffect(() => {\n'
             '  const timer = setInterval(() => {\n'
             '    setCount(count + 1);\n'
             '  }, 1000);\n'
             '\n'
             '  return () => clearInterval(timer);\n'
             '}, [count]);\n'
             '```\n'
             '\n'
             '两种方案的差异如下：\n'
             '\n'
             '| 方案 | 优点 | 代价 |\n'
             '| --- | --- | --- |\n'
             '| 函数式更新 | 不依赖外部 `count`，定时器无需反复重建 | 仅适合下一状态可由上一状态推导的场景 |\n'
             '| 完整依赖 | 与 React 数据流完全同步，闭包语义清晰 | 依赖变化会触发清理与重建副作用 |\n'
             '\n'
             '状态快照并非 React 缺陷，而是 JavaScript 闭包与函数式渲染模型共同产生的结果。关键是明确每个回调读取的是哪一次渲染中的值。\n'
             '\n'
             '**依赖项最佳实践**：Effect 依赖项应完整声明所有在 Effect 内部读取的响应式值，包括 '
             'Props、State、组件内定义的函数、派生变量等。故意省略依赖项会让 Effect 与实际数据流脱节，产生隐蔽的陈旧数据问题。\n'
             '\n'
             '依赖声明原则如下：\n'
             '\n'
             '| 原则 | 说明 |\n'
             '| --- | --- |\n'
             '| 不欺骗依赖数组 | Effect 使用了某个响应式值，就应将其加入依赖 |\n'
             '| 优先重构数据流 | 不应通过删除依赖掩盖重复请求或循环更新问题 |\n'
             '| 减少不稳定引用 | 对象、数组、函数在每次渲染都会创建新引用，可能触发额外 Effect |\n'
             '| 清理必须完整 | 订阅、定时器、外部实例、未完成请求都应在 cleanup 中释放 |\n'
             '\n'
             '减少不必要依赖刷新的常见方式如下：\n'
             '\n'
             '| 方式 | 适用场景 | 示例 |\n'
             '| --- | --- | --- |\n'
             '| 使用函数式更新 | 下一状态只依赖上一状态 | `setCount((c) => c + 1)` |\n'
             '| 使用 `useReducer` | 多个状态更新存在复杂关联 | 将状态转移逻辑收敛到 reducer |\n'
             '| 将函数移出组件 | 函数不依赖组件内部响应式值 | 模块级纯函数 |\n'
             '| 使用 `useCallback` | 函数需要作为依赖或传给子组件 | `const fn = useCallback(() => {}, [deps])` |\n'
             '| 将对象创建放入 Effect 内部 | 对象只为副作用服务 | 避免对象引用成为外部依赖 |\n'
             '\n'
             '`useReducer` 示例：\n'
             '\n'
             '```tsx\n'
             'import { useReducer } from "react";\n'
             '\n'
             'type State = {\n'
             '  count: number;\n'
             '};\n'
             '\n'
             'type Action =\n'
             '  | { type: "increment" }\n'
             '  | { type: "reset" };\n'
             '\n'
             'function reducer(state: State, action: Action): State {\n'
             '  switch (action.type) {\n'
             '    case "increment":\n'
             '      return { count: state.count + 1 };\n'
             '    case "reset":\n'
             '      return { count: 0 };\n'
             '    default:\n'
             '      return state;\n'
             '  }\n'
             '}\n'
             '\n'
             'function Counter() {\n'
             '  const [state, dispatch] = useReducer(reducer, { count: 0 });\n'
             '\n'
             '  return (\n'
             '    <button onClick={() => dispatch({ type: "increment" })}>\n'
             '      {state.count}\n'
             '    </button>\n'
             '  );\n'
             '}\n'
             '```\n'
             '\n'
             '`useCallback` 示例：\n'
             '\n'
             '```tsx\n'
             'import { useCallback, useEffect } from "react";\n'
             '\n'
             'function SearchPanel({ keyword }: { keyword: string }) {\n'
             '  const fetchResults = useCallback(async () => {\n'
             '    const response = await fetch(`/api/search?q=${encodeURIComponent(keyword)}`);\n'
             '    return response.json();\n'
             '  }, [keyword]);\n'
             '\n'
             '  useEffect(() => {\n'
             '    fetchResults();\n'
             '  }, [fetchResults]);\n'
             '\n'
             '  return null;\n'
             '}\n'
             '```\n'
             '\n'
             '`useCallback` 不是性能优化的默认答案。只有当函数引用稳定性确实影响 Effect 依赖、子组件 '
             'memo、事件订阅清理或第三方库交互时，才有明确使用价值。\n'
             '\n'
             '## 5. 路由生态 (React Router)\n'
             '\n'
             '**核心组件**：`react-router-dom` 是 React Web 应用中常用的路由库，用于将 URL '
             '路径映射到对应组件，并提供导航、参数读取、嵌套路由等能力。\n'
             '\n'
             '核心基础构成如下：\n'
             '\n'
             '| 组件 | 作用 | 说明 |\n'
             '| --- | --- | --- |\n'
             '| `<BrowserRouter>` | 路由容器 | 基于 HTML5 History API 监听 URL 变化，为内部组件提供路由上下文 |\n'
             '| `<Routes>` | 路由匹配器 | 在子 `<Route>` 中选择当前 URL 匹配度最高的路由分支 |\n'
             '| `<Route>` | 路径与组件映射 | 使用 `path` 描述路径规则，使用 `element` 指定渲染组件 |\n'
             '\n'
             '基础示例：\n'
             '\n'
             '```tsx\n'
             'import { BrowserRouter, Link, Route, Routes } from "react-router-dom";\n'
             '\n'
             'function HomePage() {\n'
             '  return <h1>首页</h1>;\n'
             '}\n'
             '\n'
             'function AboutPage() {\n'
             '  return <h1>关于</h1>;\n'
             '}\n'
             '\n'
             'function AppRouter() {\n'
             '  return (\n'
             '    <BrowserRouter>\n'
             '      <nav>\n'
             '        <Link to="/">首页</Link>\n'
             '        <Link to="/about">关于</Link>\n'
             '      </nav>\n'
             '\n'
             '      <Routes>\n'
             '        <Route path="/" element={<HomePage />} />\n'
