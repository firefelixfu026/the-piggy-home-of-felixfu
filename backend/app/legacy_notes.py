             '\n'
             '- **代码生成模型**：负责快速生成实现、测试、脚手架和迁移代码。\n'
             '- **代码审查模型**：从正确性、安全性、边界条件、性能、可维护性角度审查 diff。\n'
             '- **文档生成模型**：把实现产物转化为 README、接口说明、变更日志和学习笔记。\n'
             '- **研究检索模型**：基于官方文档、标准规范和版本说明核对 API 与最佳实践。\n'
             '\n'
             '推荐协同流程：\n'
             '\n'
             '1. 定义需求、约束和验收标准。\n'
             '2. 生成模型产出最小候选实现。\n'
             '3. 工具链执行格式化、类型检查、测试、构建。\n'
             '4. 审查模型只读取 diff、测试结果和相关文件，输出风险清单。\n'
             '5. 生成模型基于审查意见修复。\n'
             '6. 人类开发者根据业务目标和工程风险做最终验收。\n'
             '\n'
             '### 2. 开发工具大全\n'
             '\n'
             'AI 编程工具链可以按接近真实工程环境的程度划分。\n'
             '\n'
             '| 工具形态 | 典型工具 | 核心能力 | 适用场景 | 主要风险 |\n'
             '| --- | --- | --- | --- | --- |\n'
             '| Web Chat | ChatGPT、Claude、Gemini | 解释概念、生成草稿、方案讨论 | 学习、设计、文档草稿 | 缺少本地上下文，容易产生版本幻觉 '
             '|\n'
             '| IDE AI 插件 | Cursor、Windsurf、GitHub Copilot、JetBrains AI | 代码补全、局部重构、上下文问答 | '
             '日常编码、局部修改、快速跳转 | 容易局部最优，忽略跨模块影响 |\n'
             '| CLI Agent | Codex、Claude Code 类工具 | 读写文件、执行命令、运行测试、修改项目 | 工程闭环、批量重构、测试修复 | '
             '权限过大时可能误改文件 |\n'
             '| 自动化平台 | Dify、Coze、n8n、LangGraph Studio | 可视化流程、RAG 应用、业务编排 | 快速原型、业务流程验证 | '
             '状态复杂后调试困难 |\n'
             '| 框架层 | LangChain、LangGraph、LlamaIndex、AutoGen、CrewAI | 模型调用、工具注册、状态管理、多 Agent 协作 | '
             'Agent 应用开发、研究实验 | 抽象成本和框架锁定 |\n'
             '\n'
             '工具选择原则：\n'
             '\n'
             '- 小范围代码补全优先使用 IDE 插件。\n'
             '- 跨文件修改和测试修复优先使用 CLI Agent。\n'
             '- 业务流程验证可使用低代码平台快速搭建。\n'
             '- 需要稳定状态机、多步骤工具调用和多 Agent 协作时，引入代码框架。\n'
             '\n'
             'MCP（Model Context '
             'Protocol）用于标准化模型应用与外部工具、数据源和提示模板之间的连接方式。它将外部能力暴露为统一协议，减少不同客户端重复适配同一工具的成本。\n'
             '\n'
             'MCP 基本结构：\n'
             '\n'
             '```text\n'
             'MCP Client\n'
             '  -> MCP Protocol\n'
             '    -> MCP Server\n'
             '      -> Tool\n'
             '      -> Resource\n'
             '      -> Prompt\n'
             '```\n'
             '\n'
             'MCP 三大原语：\n'
             '\n'
             '- **Tool**：可执行动作，例如查询数据库、创建 Issue、运行搜索、调用业务 API。\n'
             '- **Resource**：可读取资源，例如文件、日志、文档、数据库记录、网页内容。\n'
             '- **Prompt**：可复用提示模板，例如代码审查模板、SQL 分析模板、日报生成模板。\n'
             '\n'
             'MCP Server 的工程价值：\n'
             '\n'
             '- 将外部能力包装为结构化接口。\n'
             '- 通过 schema 限制工具参数，降低误调用概率。\n'
             '- 将模型与具体系统解耦，便于跨客户端复用。\n'
             '- 允许对高风险操作增加权限检查和人工确认。\n'
             '\n'
             '示例配置：\n'
             '\n'
             '```json\n'
             '{\n'
             '  "mcpServers": {\n'
             '    "filesystem": {\n'
             '      "command": "npx",\n'
             '      "args": ["-y", "@modelcontextprotocol/server-filesystem", "./docs"]\n'
             '    }\n'
             '  }\n'
             '}\n'
             '```\n'
             '\n'
             'Skill 用于封装高频、细粒度、可复用的操作能力。与 MCP 侧重“连接外部能力”不同，Skill 侧重“固化任务方法”。一个代码审查 Skill '
             '通常包含审查优先级、命令清单、输出格式、项目约束和参考资料。\n'
             '\n'
             'Skill 的典型内容：\n'
             '\n'
             '- 任务触发条件。\n'
             '- 标准操作步骤。\n'
             '- 可复用脚本或模板。\n'
             '- 输出格式约束。\n'
             '- 领域知识和注意事项。\n'
             '\n'
             'MCP 与 Skill 的关系：\n'
             '\n'
             '```text\n'
             'MCP = 工具接入层，解决“能调用什么”\n'
             'Skill = 方法封装层，解决“应该怎样做”\n'
             '```\n'
             '\n'
             '### 3. Vibe Coding 范式导论\n'
             '\n'
             'Vibe Coding '
             '的核心不是随机对话式生成代码，而是用自然语言编排可执行的工程意图。高质量意图描述必须具备结构化信息，使模型能够正确定位任务、控制范围并输出可验证结果。\n'
             '\n'
             '自然语言编排应覆盖六类信息：\n'
             '\n'
             '1. **目标**\n'
             '   明确要完成的用户价值或工程结果，避免只描述技术动作。\n'
             '\n'
             '2. **输入输出**\n'
             '   定义用户输入、系统输出、数据结构、异常路径和边界条件。\n'
             '\n'
             '3. **关联模块**\n'
             '   指明相关文件、函数、接口、路由、数据库表、配置项和调用链。\n'
             '\n'
             '4. **约束条件**\n'
             '   说明禁止修改的文件、必须沿用的风格、性能要求、安全要求和兼容性要求。\n'
             '\n'
             '5. **验收标准**\n'
             '   明确通过哪些测试、构建命令、页面行为、接口响应或人工检查才算完成。\n'
             '\n'
             '6. **交付格式**\n'
             '   指定需要代码、测试、文档、迁移脚本、评估报告还是设计说明。\n'
             '\n'
             '推荐任务描述模板：\n'
             '\n'
             '```text\n'
             '背景：\n'
             '  项目类型、技术栈、相关目录、当前问题。\n'
             '\n'
             '目标：\n'
             '  本次变更要达成的业务结果或工程结果。\n'
             '\n'
             '输入输出：\n'
             '  输入来源、输出结构、异常处理、边界情况。\n'
             '\n'
             '关联模块：\n'
             '  涉及文件、接口、函数、数据库表、配置项。\n'
             '\n'
             '约束：\n'
             '  不能修改的内容、必须保持的兼容性、性能和安全要求。\n'
             '\n'
             '验收：\n'
             '  必须通过的命令、测试、页面行为、日志检查或人工验证项。\n'
             '```\n'
             '\n'
             '低质量任务描述：\n'
             '\n'
             '```text\n'
             '帮我做一个 AI 笔记系统。\n'
             '```\n'
             '\n'
             '高质量任务描述：\n'
             '\n'
             '```text\n'
             '背景：\n'
             '  现有项目是 MkDocs Material 知识库，文档目录为 docs/。\n'
             '\n'
             '目标：\n'
             '  新增一篇 Vibe Coding 与 Agent 学习路线笔记，文件名为 docs/vibecoding.md。\n'
             '\n'
             '约束：\n'
             '  使用标准 Markdown 标题；必须包含名词对比表；不得修改其他笔记内容。\n'
             '\n'
             '验收：\n'
             '  mkdocs build --strict 可以通过；表格渲染正常；标题层级符合 MkDocs TOC。\n'
             '```\n'
             '\n'
             '### 4. 提示词工程\n'
             '\n'
             '提示词工程的目标是降低需求歧义、控制模型输出范围、提升可验证性。高质量提示词不追求花哨表达，而追求上下文充分、目标明确、约束可执行、验收可检查。\n'
             '\n'
             '四大核心要素：\n'
             '\n'
             '| 要素 | 作用 | 关键问题 |\n'
             '| --- | --- | --- |\n'
             '| 背景 | 提供项目上下文和任务环境 | 当前系统是什么，已有约定是什么 |\n'
             '| 目标 | 定义本次任务的完成状态 | 要解决什么问题，用户价值是什么 |\n'
             '| 约束 | 限定修改范围和风险边界 | 不能改什么，必须遵循什么 |\n'
             '| 验收 | 建立客观完成标准 | 运行什么命令，检查什么行为 |\n'
             '\n'
             '提示词中的约束应尽量可执行：\n'
             '\n'
             '- “保持代码整洁”属于弱约束。\n'
             '- “沿用现有 `services/` 分层，不新增全局状态；新增逻辑必须有单元测试”属于强约束。\n'
             '\n'
             '提示词中的验收应尽量可验证：\n'
             '\n'
             '- “功能正常”属于弱验收。\n'
             '- “`npm test`、`npm run build` 通过；登录失败时返回 401；空密码返回表单校验错误”属于强验收。\n'
             '\n'
             '面向代码修改的提示词示例：\n'
             '\n'
             '```text\n'
             '背景：\n'
             '  当前项目使用 FastAPI + SQLAlchemy。用户登录逻辑位于 app/auth/。\n'
             '\n'
             '目标：\n'
             '  为登录接口增加账号锁定机制。连续 5 次密码错误后锁定 15 分钟。\n'
             '\n'
             '约束：\n'
             '  不改变现有登录响应字段；不引入新数据库；沿用现有 User 模型迁移方式。\n'
             '\n'
             '验收：\n'
             '  新增单元测试覆盖：失败次数累计、锁定期间拒绝登录、锁定过期后允许登录。\n'
             '  pytest 必须通过。\n'
             '```\n'
             '\n'
             '提示词常见错误：\n'
             '\n'
             '- 只给目标，不给上下文。\n'
             '- 只给功能，不给边界条件。\n'
             '- 只要求“优化”，不定义指标。\n'
             '- 一次要求实现、重构、换框架、补测试、写文档，导致任务范围失控。\n'
             '- 缺少验收命令，模型无法形成闭环。\n'
             '\n'
             '### 5. 最小可行产品\n'
             '\n'
             'MVP（Minimum Viable Product）是用最小实现验证核心假设的工程策略。AI 辅助开发场景中，MVP '
             '的意义更加重要：模型生成速度很快，如果没有范围控制，系统会快速堆积不可验证的复杂度。\n'
             '\n'
             'MVP 构建原则：\n'
             '\n'
             '- **单一主路径**：先实现最关键用户流程。\n'
             '- **最小依赖**：优先使用现有技术栈和轻量存储。\n'
             '- **可运行优先**：先跑通端到端流程，再完善异常和扩展能力。\n'
             '- **可观测优先**：关键路径必须有日志、错误信息或测试反馈。\n'
             '- **可删除优先**：原型代码应便于替换，避免过早绑定复杂架构。\n'
             '\n'
             '示例：AI 文档摘要工具的 MVP 范围。\n'
             '\n'
             '必须实现：\n'
             '\n'
             '1. 输入一段文档文本。\n'
             '2. 调用模型生成摘要。\n'
             '3. 输出固定结构：核心问题、关键方法、主要结论。\n'
             '4. 保存一次运行结果。\n'
             '5. 提供最小测试或手动验收脚本。\n'
             '\n'
             '暂缓实现：\n'
             '\n'
             '- 多用户系统。\n'
             '- 复杂权限模型。\n'
             '- 向量数据库。\n'
             '- 自动爬取外部文档。\n'
             '- 精细化前端交互。\n'
             '\n'
             'AI 代码审核规范：\n'
             '\n'
             '- **API 真实性**：核对官方文档、本地类型定义或依赖源码。\n'
             '- **依赖一致性**：检查 `package.json`、`requirements.txt`、`pyproject.toml`、锁文件。\n'
             '- **调用链完整性**：确认新增函数被真实调用，删除代码无残留引用。\n'
             '- **错误路径覆盖**：检查空输入、超时、权限不足、网络失败、并发冲突。\n'
             '- **安全边界**：排查密钥泄露、命令注入、SQL 注入、路径穿越、越权访问。\n'
             '- **测试有效性**：测试必须断言真实行为，避免只检查函数被调用或快照存在。\n'
             '\n'
             'AI 幻觉识别清单：\n'
             '\n'
             '- 生成了项目中不存在的文件路径。\n'
             '- 使用了依赖中不存在的函数、参数或配置字段。\n'
             '- 忽略已有架构，重新发明一套并行抽象。\n'
             '- 给出无法复现的报错解释。\n'
             '- 测试用例只适配生成代码，而不验证真实需求。\n'
             '- 文档描述与代码行为不一致。\n'
             '\n'
             '### 6. 迭代式开发循环\n'
             '\n'
             '稳定的 Vibe Coding 应采用“描述 -> 生成 -> 运行 -> 反馈 -> 修改”的闭环，而不是一次性生成大段代码。\n'
             '\n'
             '标准循环：\n'
             '\n'
             '1. **描述**\n'
             '   明确背景、目标、约束、验收。\n'
             '\n'
             '2. **生成**\n'
             '   生成最小可审查 diff，避免无关重构。\n'
             '\n'
             '3. **运行**\n'
             '   执行格式化、静态检查、单元测试、集成测试、构建或启动验证。\n'
             '\n'
             '4. **反馈**\n'
             '   将真实错误日志、失败测试、截图、diff 和运行环境返回给模型。\n'
             '\n'
             '5. **修改**\n'
             '   基于证据修复问题，避免基于猜测扩大改动范围。\n'
             '\n'
             '6. **沉淀**\n'
             '   将重复流程固化为脚本、Hook、CI、Skill 或文档。\n'
             '\n'
             '典型命令链：\n'
             '\n'
             '```bash\n'
             'npm run format\n'
             'npm run lint\n'
             'npm test\n'
             'npm run build\n'
             '```\n'
             '\n'
             'MkDocs 项目验收命令：\n'
             '\n'
             '```bash\n'
             'mkdocs build --strict\n'
             '```\n'
             '\n'
             'Hooks 用于把高频检查自动化，降低人为遗漏。常见 Hook 类型：\n'
             '\n'
             '- **格式化 Hook**：Prettier、Black、Ruff format、go fmt。\n'
             '- **静态检查 Hook**：ESLint、Ruff、mypy、tsc。\n'
             '- **测试 Hook**：pytest、vitest、go test。\n'
             '- **构建 Hook**：Vite build、MkDocs build、Docker build。\n'
             '- **提交规范 Hook**：commitlint、secret scan、YAML/JSON 校验。\n'
             '\n'
             '`pre-commit` 示例：\n'
             '\n'
             '```yaml\n'
             'repos:\n'
             '  - repo: https://github.com/pre-commit/pre-commit-hooks\n'
             '    rev: v4.6.0\n'
             '    hooks:\n'
             '      - id: trailing-whitespace\n'
             '      - id: end-of-file-fixer\n'
             '      - id: check-yaml\n'
             '      - id: check-json\n'
             '      - id: detect-private-key\n'
             '```\n'
             '\n'
             '长上下文管理的工程演进路径：\n'
             '\n'
             '1. **文件级上下文**\n'
             '   适合小修改。只提供当前文件、相关测试和错误日志。\n'
             '\n'
             '2. **模块级上下文**\n'
             '   适合跨文件功能。提供接口定义、调用链、数据模型和测试入口。\n'
             '\n'
             '3. **项目级规则**\n'
             '   适合长期维护。使用 `README`、`CONTRIBUTING`、架构文档、ADR 记录稳定约束。\n'
             '\n'
             '4. **RAG**\n'
             '   适合大规模文档和代码知识库。通过检索相关片段降低上下文窗口压力。\n'
             '\n'
             '5. **SubAgent**\n'
             '   适合并行分析、审查、测试、研究。通过角色隔离减少上下文污染。\n'
             '\n'
             '6. **Agent Team**\n'
             '   适合复杂工程任务。通过 Planner、Coder、Reviewer、Researcher、QA、Writer 等角色协作形成多步骤闭环。\n'
             '\n'
             'RAG（Retrieval-Augmented Generation）流程：\n'
             '\n'
             '```text\n'
             '文档切分 -> 向量化 -> 相似度检索 -> 重排序 -> 上下文注入 -> 基于证据生成\n'
             '```\n'
             '\n'
             'RAG 核心痛点：\n'
             '\n'
             '- chunk 粒度影响召回和噪声。\n'
             '- embedding 相似度不等于任务相关性。\n'
             '- 检索结果需要重排序和去重。\n'
             '- 生成阶段必须约束模型基于证据回答。\n'
             '- 知识库更新后索引必须同步刷新。\n'
             '\n'
             'SubAgent 与 Agent Team 的引入条件：\n'
             '\n'
             '- 任务可并行拆分。\n'
             '- 子任务边界明确。\n'
             '- 需要上下文隔离。\n'
             '- 需要多视角审查。\n'
             '- 汇总成本低于并行收益。\n'
             '\n'
             '典型 Agent Team：\n'
             '\n'
             '```text\n'
             'Planner Agent：拆解任务、定义依赖关系和验收路线\n'
             'Coder Agent：实现代码和局部测试\n'
             'Reviewer Agent：审查正确性、安全性和回归风险\n'
             'Research Agent：核对官方文档和版本差异\n'
             'QA Agent：运行测试、复现用户路径、整理失败证据\n'
             'Writer Agent：生成文档、变更说明和使用示例\n'
             '```\n'
             '\n'
             '## 名词概念速通\n'
             '\n'
             '| 名词 | 技术本质/定义 | 一句话通俗理解 | 在 Vibe Coding 中的角色 |\n'
             '| --- | --- | --- | --- |\n'
             '| LLM | 基于大规模语料训练的生成模型，通过上下文预测 token，并经过指令微调具备问答、代码生成和推理能力。 | '
             '能根据上下文生成高概率文本和代码的通用生成引擎。 | 负责代码生成、错误解释、方案草拟、测试补全和文档生成。 |\n'
             '| Function Calling | 模型输出结构化函数名和参数，由外部程序执行真实函数并返回结果。 | 让模型从“说答案”升级为“调用工具办事”。 | '
             '将自然语言意图转化为 API 调用、数据库查询、文件操作或脚本执行。 |\n'
             '| Agent | 带目标、状态、工具、记忆和反馈循环的任务执行系统。 | 能围绕目标连续行动的 LLM 应用。 | '
             '串联需求分析、代码修改、测试运行、错误修复和结果汇报。 |\n'
             '| SubAgent | 为特定子任务隔离上下文、职责和工具权限的子智能体。 | 专门处理一个局部任务的隔离执行单元。 | '
             '用于并行研究、代码审查、测试验证、文档生成和复杂任务拆解。 |\n'
             '| MCP | Model Context Protocol，标准化模型应用连接外部工具、资源和提示模板的协议。 | AI 应用连接外部世界的统一插座。 | 让不同 '
             'AI 客户端复用文件系统、数据库、搜索、知识库等能力。 |\n'
             '| Tool | MCP 原语之一，表示模型可触发的可执行动作。 | 可以被模型调用的函数或操作。 | 执行搜索、读写文件、创建任务、查询数据库、运行业务 API。 '
             '|\n'
             '| Resource | MCP 原语之一，表示模型可读取的上下文资源。 | 提供给模型参考的外部材料。 | 注入项目文件、日志、文档、数据库记录、网页内容。 |\n'
             '| Prompt | MCP 原语之一，表示可复用的提示模板。 | 固化任务格式和操作套路的模板。 | 统一代码审查、SQL 分析、文档生成、调试流程的输入结构。 |\n'
             '| MCP Server | 暴露 Tool、Resource、Prompt 的服务端组件，供 MCP Client 连接和调用。 | '
             '把外部系统包装成模型可理解接口的适配层。 | 将文件系统、GitHub、数据库、浏览器、企业系统接入 Agent。 |\n'
             '| Workflow | 将任务拆成节点、状态、条件分支、工具调用和停止条件的流程结构。 | 给 Agent 规定可观察、可回退、可调试的执行路线。 | '
             '约束模型行为，避免无限循环和无边界自由生成。 |\n'
             '| Langchain | 面向 LLM 应用的组件框架，提供模型调用、Prompt、工具、检索、链式组合等抽象。 | LLM 应用开发的工具箱。 | 快速搭建 '
             'RAG、工具调用、文档问答和原型 Agent。 |\n'
             '| LangGraph | 基于图结构和状态机思想的 Agent 工作流框架，适合循环、分支、多 Agent 和人工介入。 | 用图来描述 Agent 的状态流转。 | '
             '构建可控的多步骤 Agent、复杂 Workflow 和多代理协同系统。 |\n'
             '| Skill | 将高频任务的流程、脚本、模板和领域知识封装为可复用能力。 | 给 Agent 的专项操作手册。 | '
             '固化代码审查、测试修复、文档生成、部署检查等高频动作。 |\n'
             '| 可视化编排 | 通过图形界面配置节点、条件、工具和数据流的工作流搭建方式。 | 用拖拽节点搭 Agent 流程。 | '
             '适合业务原型、非复杂状态流程和快速验证，但复杂逻辑需要代码化治理。 |\n'
             '\n'
             '## DataWhale: hello-agent 路线\n'
             '\n'
             '### 1. 基础主线\n'
             '\n'
             'DataWhale `hello-agents` 路线围绕“从零开始构建智能体”展开，适合从基础概念过渡到工程实现。\n'
             '\n'
             '智能体发展史可按技术范式划分：\n'
             '\n'
             '1. **符号主义与专家系统**\n'
             '   依赖人工规则、知识库和推理引擎，优点是可解释，缺点是知识获取成本高、泛化能力弱。\n'
             '\n'
             '2. **机器学习与强化学习智能体**\n'
             '   通过数据或环境反馈学习策略，适合明确奖励函数场景，但在开放文本任务和通用工具使用上受限。\n'
             '\n'
             '3. **LLM 驱动的工具型 Agent**\n'
             '   借助语言模型理解任务、生成计划，并通过 Function Calling 或 Tool Use 调用外部工具。\n'
             '\n'
             '4. **多 Agent 与社会模拟**\n'
             '   多个 Agent 通过通信、分工、协作和环境反馈完成复杂任务或模拟群体行为。\n'
             '\n'
             '大模型基础应覆盖：\n'
             '\n'
             '- token 与上下文窗口。\n'
             '- temperature、top-p 等解码参数。\n'
             '- embedding 与语义检索。\n'
             '- 指令微调与对齐。\n'
             '- 函数调用与工具使用。\n'
             '- 上下文压缩与长程依赖问题。\n'
             '\n'
             '经典 Agent 范式：\n'
             '\n'
             '- **ReAct**：Reasoning 与 Acting 交替执行，通过“思考 -> 行动 -> 观察”形成反馈循环。\n'
             '- **Plan-and-Execute**：先生成计划，再逐步执行，适合长任务。\n'
             '- **Reflection**：执行后自评和修正，适合复杂推理和代码修复。\n'
             '- **Tool Use**：根据任务动态选择工具，将模型输出落到真实环境。\n'
             '- **Multi-Agent Debate**：多个 Agent 从不同角度讨论或审查，降低单点偏差。\n'
             '\n'
             '### 2. 框架与实践\n'
             '\n'
             '主流 Agent 框架通常提供以下能力：\n'
             '\n'
             '- 模型调用适配。\n'
             '- Prompt 模板管理。\n'
             '- Tool schema 定义。\n'
             '- Memory 与 Retrieval。\n'
             '- Agent Loop。\n'
             '- Workflow 状态管理。\n'
             '- 多 Agent 通信。\n'
             '- Trace、日志和评估。\n'
             '\n'
             '常见框架定位：\n'
             '\n'
             '| 框架 | 定位 | 适用场景 |\n'
             '| --- | --- | --- |\n'
             '| LangChain | LLM 应用组件库 | RAG、工具调用、文档问答、快速原型 |\n'
             '| LangGraph | 状态图与 Agent Workflow | 多步骤任务、循环、分支、多 Agent、人工审批 |\n'
             '| LlamaIndex | 数据连接与检索增强 | 文档索引、知识库问答、企业数据检索 |\n'
             '| AutoGen | 多 Agent 对话与协作 | 多角色协作、研究原型、任务分解 |\n'
             '| CrewAI | 角色化 Agent Team | 业务流程、多角色任务执行 |\n'
             '| Dify / Coze | 低代码 Agent 平台 | 快速搭建应用、工作流原型、知识库问答 |\n'
             '\n'
             '低代码搭建边界：\n'
             '\n'
             '- 适合验证流程，不适合承载复杂状态机。\n'
             '- 适合固定业务链路，不适合强定制调度逻辑。\n'
             '- 适合快速演示，不适合缺少测试、版本控制和权限治理的生产系统。\n'
             '- 当节点数量快速膨胀、数据结构频繁变化、错误难以复现时，应转向代码化框架。\n'
             '\n'
             '自主构建 Agent 框架的最小结构：\n'
             '\n'
             '```text\n'
             'agent/\n'
             '  llm_client.py      # 模型调用\n'
             '  tool_registry.py   # 工具注册和 schema\n'
             '  prompt_builder.py  # 上下文与提示词组装\n'
             '  memory_store.py    # 短期/长期记忆\n'
             '  loop.py            # plan-act-observe-update-stop\n'
             '  tracing.py         # 调用链记录与调试\n'
             '  eval.py            # 任务评估\n'
             '```\n'
             '\n'
             'Agent Loop 伪代码：\n'
             '\n'
             '```python\n'
             'while not done:\n'
             '    prompt = build_prompt(state, tools, memory)\n'
             '    decision = llm.generate(prompt)\n'
             '    action = parse_action(decision)\n'
             '    observation = execute_tool(action)\n'
             '    state = update_state(state, action, observation)\n'
             '    done = should_stop(state)\n'
             '```\n'
             '\n'
             '智能体性能评估（Evaluation）用于判断系统是否真的变强，而不是只在单次演示中表现良好。\n'
             '\n'
             '核心评估维度：\n'
             '\n'
             '| 维度 | 指标 | 说明 |\n'
             '| --- | --- | --- |\n'
             '| 任务成功率 | success rate、人工评分 | 最终目标是否完成 |\n'
             '| 工具调用质量 | tool accuracy、invalid calls | 工具选择和参数是否正确 |\n'
             '| 过程效率 | latency、token cost、step count | 时间、成本和步骤数是否可接受 |\n'
             '| 稳定性 | pass@k、结果方差 | 多次运行是否一致 |\n'
             '| 安全性 | policy violation、blocked actions | 是否越权、泄露或误操作 |\n'
             '| 可解释性 | trace completeness | 是否能复盘每一步决策 |\n'
             '\n'
             '评估集应包含正常任务、边界任务、对抗输入、工具失败、信息不足和需要人工确认的任务。没有固定评估集的 Agent 只能算 Demo，不能证明能力提升。\n'
             '\n'
             '### 3. 记忆、检索与上下文\n'
             '\n'
             'Memory & Retrieval 是 Agent 从单轮问答走向长期任务执行的关键组件。\n'
             '\n'
             '记忆类型：\n'
             '\n'
             '- **短期记忆**：当前对话、当前任务状态、最近工具观察。\n'
             '- **长期记忆**：用户偏好、项目规范、历史决策、稳定事实。\n'
             '- **情景记忆**：特定事件的时间、地点、参与者和结果。\n'
             '- **语义记忆**：可检索的知识片段，如文档、代码、笔记。\n'
             '- **程序性记忆**：固定操作流程，可沉淀为 Skill 或 Workflow。\n'
             '\n'
             '记忆系统的关键难点：\n'
             '\n'
             '- 写入什么：不是所有对话都值得进入长期记忆。\n'
             '- 何时检索：检索触发条件需要与任务意图绑定。\n'
             '- 检索多少：过少导致信息缺失，过多导致噪声污染。\n'
             '- 如何排序：需要基于相关性、新鲜度、可信度和任务阶段综合排序。\n'
             '- 如何遗忘：过期偏好、错误事实和临时状态必须可清理。\n'
             '\n'
             'Retrieval 典型流水线：\n'
             '\n'
             '```text\n'
             'query rewrite\n'
             '  -> hybrid search\n'
             '  -> rerank\n'
             '  -> deduplicate\n'
             '  -> filter stale context\n'
             '  -> inject evidence\n'
             '  -> grounded generation\n'
             '```\n'
             '\n'
             'Context Engineering 是比“扩大上下文窗口”更重要的工程能力。上下文不是越多越好，而是需要让模型在每一步看到最小充分信息。\n'
             '\n'
             '上下文应包含：\n'
             '\n'
             '- 当前目标。\n'
             '- 当前状态。\n'
             '- 相关代码或文档。\n'
             '- 可用工具及参数 schema。\n'
             '- 禁止行为。\n'
             '- 最近观察结果。\n'
             '- 验收标准。\n'
             '\n'
             '上下文应避免：\n'
             '\n'
             '- 无关历史对话。\n'
             '- 重复或过期文档。\n'
             '- 未验证猜测。\n'
             '- 大量无关代码。\n'
             '- 敏感信息。\n'
             '\n'
             '有效上下文设计原则：\n'
             '\n'
             '- **结构化**：用明确字段组织目标、事实、约束和观察。\n'
             '- **可追溯**：检索内容带来源，便于核查。\n'
             '- **可压缩**：长任务阶段性总结，保留决策和状态。\n'
             '- **可更新**：工具结果、测试反馈和用户变更及时覆盖旧信息。\n'
             '- **最小充分**：只注入完成当前步骤所需上下文。\n'
             '\n'
             '### 4. 工具、协议与协作\n'
             '\n'
             '工具调用使 Agent 从文本生成系统变成可执行系统。协议和协作机制决定多 Agent 系统是否可控。\n'
             '\n'
             '工具设计原则：\n'
             '\n'
             '- 参数必须有 schema。\n'
             '- 返回值必须结构化。\n'
             '- 错误必须显式表达。\n'
             '- 高风险动作需要人工确认。\n'
             '- 工具权限遵循最小权限原则。\n'
             '- 工具调用过程必须记录 trace。\n'
             '\n'
             '多 Agent 通信协议应避免纯自然语言自由传递，推荐使用结构化消息。\n'
             '\n'
             '示例：\n'
             '\n'
             '```json\n'
             '{\n'
             '  "sender": "reviewer",\n'
             '  "receiver": "coder",\n'
             '  "task_id": "auth-lockout",\n'
             '  "message_type": "review_findings",\n'
             '  "severity": "high",\n'
             '  "findings": [\n'
             '    {\n'
             '      "file": "app/auth/service.py",\n'
             '      "issue": "failed login counter is not reset after successful login",\n'
             '      "expected_fix": "reset counter and add regression test"\n'
             '    }\n'
             '  ]\n'
             '}\n'
             '```\n'
             '\n'
             '多 Agent 协作机制：\n'
             '\n'
             '- **角色分工**：Planner、Coder、Reviewer、Researcher、QA、Writer。\n'
             '- **上下文隔离**：每个 Agent 只读取必要信息。\n'
             '- **消息协议**：使用结构化字段传递任务、证据、状态和请求。\n'
             '- **冲突解决**：由主 Agent 或人类决策者合并冲突结论。\n'
             '- **统一评估**：所有 Agent 输出进入同一验收链路。\n'
             '\n'
             '智能旅行助手系统架构：\n'
             '\n'
             '```text\n'
             'User Request\n'
             '  -> Preference Parser\n'
             '  -> Planner Agent\n'
             '  -> Tool Layer\n'
             '       -> Search API\n'
             '       -> Map API\n'
             '       -> Weather API\n'
             '       -> Calendar API\n'
             '       -> Budget Calculator\n'
             '  -> Constraint Checker\n'
             '  -> Itinerary Generator\n'
             '  -> User Feedback Loop\n'
             '```\n'
             '\n'
             '关键工程点：\n'
             '\n'
             '- 外部信息强时效性，需要实时检索。\n'
             '- 用户偏好可能冲突，需要约束求解。\n'
             '- 工具结果格式不统一，需要规范化中间表示。\n'
             '- 输出必须可执行，而不是只生成流畅文案。\n'
             '\n'
             '赛博小镇（Generative Agents）系统架构：\n'
             '\n'
             '```text\n'
             'Environment\n'
             '  -> Observation\n'
             '  -> Memory Write\n'
             '  -> Memory Retrieval\n'
             '  -> Reflection\n'
             '  -> Planning\n'
             '  -> Action\n'
             '  -> Environment Update\n'
             '  -> Other Agents Observe\n'
             '```\n'
             '\n'
             '核心模块：\n'
             '\n'
             '- **环境状态**：地点、时间、事件、对象。\n'
             '- **角色状态**：身份、目标、关系、计划。\n'
             '- **记忆系统**：事件写入、重要性评分、检索。\n'
             '- **反思机制**：从多条记忆抽象出高层信念。\n'
             '- **计划器**：生成日程和局部行动。\n'
             '- **交互协议**：Agent 与环境、Agent 与 Agent 的通信格式。\n'
             '\n'
             'Generative Agents 的难点包括状态爆炸、记忆污染、成本控制、行为一致性和可解释性。\n'
             '\n'
             '## 推荐学习顺序\n'
             '\n'
             '1. **Vibe Coding 实用工程路线**\n'
             '\n'
             '   适合目标是快速提升开发交付效率的学习者。\n'
             '\n'
             '   1. LLM 基础：token、上下文、幻觉、解码参数。\n'
             '   2. 工具链：Cursor、Windsurf、CLI Agent、Git、测试框架。\n'
             '   3. 提示词工程：背景、目标、约束、验收。\n'
             '   4. MVP：最小用户路径、最小依赖、最小测试。\n'
             '   5. 自动化闭环：格式化、静态检查、测试、构建、Hook。\n'
             '   6. RAG：为项目文档、知识库和代码上下文提供检索能力。\n'
             '   7. Agent Team：将生成、审查、研究、测试和文档拆成独立角色。\n'
             '\n'
             '2. **Agent 系统理论研究路线**\n'
             '\n'
             '   适合目标是理解底层协议、框架演进和多代理协同的学习者。\n'
             '\n'
             '   1. LLM：生成机制、上下文窗口、embedding、对齐。\n'
             '   2. Function Calling：工具 schema、结构化参数、调用结果回传。\n'
             '   3. Agent Loop：plan、act、observe、update、stop。\n'
             '   4. MCP：Tool、Resource、Prompt、MCP Server。\n'
             '   5. Workflow：ReAct、Plan-and-Execute、Reflection、Human-in-the-loop。\n'
             '   6. 框架：LangChain、LangGraph、LlamaIndex、AutoGen、CrewAI。\n'
             '   7. Memory & Retrieval：长期记忆、RAG、上下文压缩、证据引用。\n'
             '   8. Multi-Agent：通信协议、角色分工、冲突解决、协同评估。\n'
             '\n'
             '## 最后记法\n'
             '\n'
             'Vibe Coding 与 Agent 工程可以用一个职责闭环记忆：\n'
             '\n'
             '```text\n'
             '人定义目标与验收\n'
             '  -> 模型生成候选方案\n'
             '  -> 工具连接真实环境\n'
             '  -> Agent 串联多步骤执行\n'
             '  -> 上下文工程控制信息流\n'
             '  -> 自动化测试与评估提供反馈\n'
             '  -> 人做最终取舍\n'
             '```\n'
             '\n'
             '各组件职责：\n'
             '\n'
             '- **人**：定义问题、设置边界、审查风险、决定取舍。\n'
             '- **模型**：生成代码、解释错误、提出方案、补全文档。\n'
             '- **工具**：执行命令、读写文件、查询系统、调用 API。\n'
             '- **Agent**：维护状态、选择工具、组织步骤、处理反馈。\n'
             '- **上下文工程**：选择信息、压缩历史、注入证据、避免污染。\n'
             '- **评估系统**：度量成功率、成本、稳定性、安全性和可解释性。\n'
