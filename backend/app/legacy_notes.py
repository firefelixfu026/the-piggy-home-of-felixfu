             '    max_overflow=20,\n'
             '    pool_pre_ping=True,\n'
             ')\n'
             '\n'
             'AsyncSessionLocal = async_sessionmaker(engine, expire_on_commit=False)\n'
             '```\n'
             '\n'
             'Repository 示例：\n'
             '\n'
             '```python\n'
             'from sqlalchemy import select\n'
             'from sqlalchemy.ext.asyncio import AsyncSession\n'
             '\n'
             '\n'
             'async def get_user_by_email(db: AsyncSession, email: str) -> User | None:\n'
             '    result = await db.execute(select(User).where(User.email == email))\n'
             '    return result.scalar_one_or_none()\n'
             '```\n'
             '\n'
             '**参数绑定的重要性**：不要用字符串拼接 SQL。攻击者可能把输入变成 SQL 片段，造成 SQL 注入。ORM 或 SQLAlchemy Core '
             '会把用户输入当作参数，而不是当作 SQL 语法。\n'
             '\n'
             '## 5. 事务、并发与锁\n'
             '\n'
             '### 5.1 事务为什么重要\n'
             '\n'
             '**事务**是一组要么全部成功、要么全部失败的数据库操作。经典例子是转账：\n'
             '\n'
             '```text\n'
             '1. A 账户扣 100\n'
             '2. B 账户加 100\n'
             '\n'
             '如果第一步成功后系统崩溃，第二步没执行，钱就凭空消失。\n'
             '事务让这两步绑定为一个整体。\n'
             '```\n'
             '\n'
             'ACID 四个特性：\n'
             '\n'
             '| 特性 | 含义 | 直观理解 |\n'
             '| --- | --- | --- |\n'
             '| 原子性 | 全部成功或全部失败 | 不允许半次转账 |\n'
             '| 一致性 | 操作前后约束成立 | 总金额不变 |\n'
             '| 隔离性 | 并发事务互不破坏 | 多人同时操作不串账 |\n'
             '| 持久性 | 提交后结果保存 | 提交成功后数据不丢 |\n'
             '\n'
             'FastAPI 中的事务示例：\n'
             '\n'
             '```python\n'
             'from sqlalchemy.ext.asyncio import AsyncSession\n'
             '\n'
             '\n'
             'async def transfer(db: AsyncSession, from_id: int, to_id: int, amount: int) -> '
             'None:\n'
             '    async with db.begin():\n'
             '        from_account = await get_account_for_update(db, from_id)\n'
             '        to_account = await get_account_for_update(db, to_id)\n'
             '\n'
             '        if from_account.balance < amount:\n'
             '            raise ValueError("insufficient balance")\n'
             '\n'
             '        from_account.balance -= amount\n'
             '        to_account.balance += amount\n'
             '```\n'
             '\n'
             '### 5.2 行级锁 Row-level Lock\n'
             '\n'
             '**为什么需要锁**：假设商品库存只剩 1 件，两个请求几乎同时下单。如果两个请求都先读到库存为 1，再分别扣减，就可能卖出 2 件。\n'
             '\n'
             '错误流程：\n'
             '\n'
             '```text\n'
             '请求 A 读取库存 = 1\n'
             '请求 B 读取库存 = 1\n'
             '请求 A 扣减库存 -> 0\n'
             '请求 B 扣减库存 -> 0\n'
             '\n'
             '结果：两个订单都成功，库存数据却只扣了一次\n'
             '```\n'
             '\n'
             '行级锁的做法是：当一个事务正在修改某一行时，其他事务必须等待或失败，直到锁释放。\n'
             '\n'
             'PostgreSQL 常用写法：\n'
             '\n'
             '```sql\n'
             'SELECT id, stock\n'
             'FROM products\n'
             'WHERE id = 100\n'
             'FOR UPDATE;\n'
             '```\n'
             '\n'
             'SQLAlchemy 示例：\n'
             '\n'
             '```python\n'
             'from sqlalchemy import select\n'
             '\n'
             '\n'
             'async def get_product_for_update(db: AsyncSession, product_id: int) -> Product:\n'
             '    result = await db.execute(\n'
             '        select(Product)\n'
             '        .where(Product.id == product_id)\n'
             '        .with_for_update()\n'
             '    )\n'
             '    return result.scalar_one()\n'
             '```\n'
             '\n'
             '下单扣库存：\n'
             '\n'
             '```python\n'
             'async def create_order(db: AsyncSession, user_id: int, product_id: int) -> Order:\n'
             '    async with db.begin():\n'
             '        product = await get_product_for_update(db, product_id)\n'
             '\n'
             '        if product.stock <= 0:\n'
             '            raise ValueError("out of stock")\n'
             '\n'
             '        product.stock -= 1\n'
             '        order = Order(user_id=user_id, product_id=product_id)\n'
             '        db.add(order)\n'
             '        return order\n'
             '```\n'
             '\n'
             '**关键原则**：\n'
             '\n'
             '- 锁要放在事务里，否则查询结束后锁就没有意义。\n'
             '- 事务要尽量短，避免长时间占用锁。\n'
             '- 先锁再判断，不要先判断再锁。\n'
             '- 多张表加锁时保持固定顺序，降低死锁概率。\n'
             '\n'
             '### 5.3 隔离级别\n'
             '\n'
             '隔离级别决定并发事务之间能看到多少彼此的中间状态。\n'
             '\n'
             '| 隔离级别 | 能解决的问题 | 代价 |\n'
             '| --- | --- | --- |\n'
             '| Read Committed | 避免读到未提交数据 | PostgreSQL 默认，适合多数业务 |\n'
             '| Repeatable Read | 同一事务内多次读取一致 | 更强隔离，可能增加冲突 |\n'
             '| Serializable | 接近串行执行 | 最严格，吞吐下降更明显 |\n'
             '\n'
             '初学阶段先记住：**事务越严格，并发越安全，但性能成本越高**。实际项目要根据业务风险选择，而不是一律开最高级别。\n'
             '\n'
             '## 6. 登录、鉴权与安全\n'
             '\n'
             '### 6.1 Cookie 与 Session\n'
             '\n'
             '**Cookie** 是浏览器保存并自动携带的一小段文本。服务端可以通过 `Set-Cookie` 设置 '
             'Cookie，浏览器之后访问匹配域名和路径的接口时会自动带上。\n'
             '\n'
             '常见 Cookie 属性：\n'
             '\n'
             '| 属性 | 作用 |\n'
             '| --- | --- |\n'
             '| `HttpOnly` | 禁止前端 JavaScript 读取，降低 XSS 窃取风险 |\n'
             '| `Secure` | 只在 HTTPS 下发送 |\n'
             '| `SameSite` | 限制跨站请求是否携带 Cookie，降低 CSRF 风险 |\n'
             '| `Max-Age` / `Expires` | 控制过期时间 |\n'
             '| `Domain` / `Path` | 控制作用范围 |\n'
             '\n'
             '**Session** 的流程：\n'
             '\n'
             '```text\n'
             '用户登录成功\n'
             '  -> 服务端生成 session_id\n'
             '  -> session_id 存入 Redis / 数据库\n'
             '  -> 浏览器保存 session_id Cookie\n'
             '  -> 后续请求自动携带 Cookie\n'
             '  -> 服务端查询 session_id 得到用户身份\n'
             '```\n'
             '\n'
             '优点是服务端可以主动删除 Session，实现强制下线。缺点是每次鉴权都要查存储，多服务部署时要共享 Session 存储。\n'
             '\n'
             '### 6.2 Token 与 JWT\n'
             '\n'
             '**Token** 是服务端签发给客户端的身份凭证。客户端在后续请求中携带 Token，服务端验证后识别用户。\n'
             '\n'
             '常见携带方式：\n'
             '\n'
             '```http\n'
             'Authorization: Bearer <access_token>\n'
             '```\n'
             '\n'
             '**JWT** 是一种自包含 Token，通常由三段组成：\n'
             '\n'
             '```text\n'
             'header.payload.signature\n'
             '```\n'
             '\n'
             'JWT 的优点：\n'
             '\n'
             '- 服务端可以不查 Session 存储，直接验证签名。\n'
             '- 适合分布式 API 服务。\n'
             '- 可以在 payload 中放少量非敏感声明，例如用户 ID、权限范围、过期时间。\n'
             '\n'
             'JWT 的缺点：\n'
             '\n'
             '- 签发后在过期前通常不容易主动撤销。\n'
             '- payload 只是编码，不是加密，不能放密码、手机号等敏感信息。\n'
             '- Token 泄露后，攻击者可以在过期前冒用。\n'
             '\n'
             '### 6.3 密码存储\n'
             '\n'
             '**永远不要明文存密码**。数据库中应该保存密码哈希，而不是原始密码。\n'
             '\n'
             '推荐思路：\n'
             '\n'
             '```text\n'
             '用户密码\n'
             '  -> 加盐\n'
             '  -> 慢哈希算法\n'
             '  -> 保存 password_hash\n'
             '```\n'
             '\n'
             'FastAPI 常见写法：\n'
             '\n'
             '```python\n'
             'from passlib.context import CryptContext\n'
             '\n'
             'pwd_context = CryptContext(schemes=["bcrypt"], deprecated="auto")\n'
             '\n'
             '\n'
             'def hash_password(password: str) -> str:\n'
             '    return pwd_context.hash(password)\n'
             '\n'
             '\n'
             'def verify_password(password: str, password_hash: str) -> bool:\n'
             '    return pwd_context.verify(password, password_hash)\n'
             '```\n'
             '\n'
             '**为什么要慢哈希**：普通哈希太快，攻击者拿到数据库后可以高速撞库。bcrypt、argon2 这类算法故意计算较慢，用时间成本降低暴力破解成功率。\n'
             '\n'
             '### 6.4 CSRF、XSS 与 CORS\n'
             '\n'
             '**CSRF**：用户已经登录 A 网站，恶意 B 网站诱导浏览器向 A 网站发请求，因为浏览器会自动带 A 的 Cookie，A 可能误以为是用户本人操作。\n'
             '\n'
             '防护方式：\n'
             '\n'
             '- Cookie 设置 `SameSite=Lax` 或 `Strict`。\n'
             '- 关键写操作使用 CSRF Token。\n'
             '- 后端校验 `Origin` / `Referer`。\n'
             '- 不把所有跨域来源都放开。\n'
             '\n'
             '**XSS**：攻击者把恶意脚本注入页面，脚本在用户浏览器中执行。\n'
             '\n'
             '防护方式：\n'
             '\n'
             '- 前端输出时转义用户内容。\n'
             '- Cookie 使用 `HttpOnly`。\n'
             '- 后端不要把用户输入直接拼进 HTML。\n'
             '- 配置合理的 Content Security Policy。\n'
             '\n'
             '**CORS 不是鉴权机制**。CORS 是浏览器约束，不是服务端安全边界。真正的权限仍然要靠 Token、Session、角色权限、资源归属校验。\n'
             '\n'
             '## 7. 缓存、Redis 与一致性\n'
             '\n'
             '### 7.1 为什么需要缓存\n'
             '\n'
             '**缓存是空间换时间**：把高频读取、计算成本高或数据库压力大的数据放到 Redis 这类内存系统中，减少数据库访问。\n'
             '\n'
             '典型场景：\n'
             '\n'
             '- 热门文章、热门商品详情。\n'
             '- 用户会话 Session。\n'
             '- 验证码、短期 Token。\n'
             '- 排行榜、计数器。\n'
             '- 分布式锁。\n'
             '\n'
             '### 7.2 Cache Aside 模式\n'
             '\n'
             '最常见的缓存读取流程：\n'
             '\n'
             '```text\n'
             '请求数据\n'
             '  -> 先查 Redis\n'
             '  -> 命中：直接返回\n'
             '  -> 未命中：查询 PostgreSQL\n'
             '  -> 写入 Redis，并设置 TTL\n'
             '  -> 返回数据\n'
             '```\n'
             '\n'
             'Python 示例：\n'
             '\n'
             '```python\n'
             'import json\n'
             '\n'
             '\n'
             'async def get_product_detail(product_id: int):\n'
             '    cache_key = f"product:{product_id}"\n'
             '    cached = await redis.get(cache_key)\n'
             '\n'
             '    if cached:\n'
             '        return json.loads(cached)\n'
             '\n'
             '    product = await product_repo.get_by_id(product_id)\n'
             '    if product is None:\n'
             '        return None\n'
             '\n'
             '    data = product.to_dict()\n'
             '    await redis.set(cache_key, json.dumps(data), ex=300)\n'
             '    return data\n'
             '```\n'
             '\n'
             '### 7.3 缓存一致性\n'
             '\n'
             '**一致性问题**：数据库更新了，缓存里还是旧数据，用户可能读到过期结果。\n'
             '\n'
             '常见策略：\n'
             '\n'
             '| 策略 | 做法 | 适用场景 |\n'
             '| --- | --- | --- |\n'
             '| 先更新数据库，再删除缓存 | 写操作成功后让缓存失效 | 常用，简单稳妥 |\n'
             '| 设置 TTL | 缓存自动过期 | 容忍短暂旧数据 |\n'
             '| 延迟双删 | 删除缓存、更新数据库、短暂等待后再删一次 | 降低并发读写下的脏缓存概率 |\n'
             '| 消息队列同步 | 数据变更后异步通知缓存失效 | 更复杂的分布式系统 |\n'
             '\n'
             '常用写法：\n'
             '\n'
             '```python\n'
             'async def update_product(product_id: int, payload: ProductUpdate):\n'
             '    async with db.begin():\n'
             '        product = await product_repo.update(product_id, payload)\n'
             '\n'
             '    await redis.delete(f"product:{product_id}")\n'
             '    return product\n'
             '```\n'
             '\n'
             '**为什么通常删除缓存，而不是直接更新缓存**：删除缓存让下一次读取从数据库重新加载，可以避免多个写请求并发时互相覆盖缓存。\n'
             '\n'
             '### 7.4 Redis 分布式锁\n'
             '\n'
             '分布式锁用于“同一时间只允许一个进程执行某段逻辑”。例如定时任务去重、库存扣减保护、避免重复支付回调处理。\n'
             '\n'
             '基础思路：\n'
             '\n'
             '```text\n'
             'SET lock:order:123 random_value NX EX 10\n'
             '\n'
             'NX: 只有 key 不存在才设置\n'
             'EX: 设置过期时间，避免服务崩溃后死锁\n'
             'random_value: 解锁时校验，避免删掉别人的锁\n'
             '```\n'
             '\n'
             '释放锁时要保证“校验 value + 删除 key”是原子操作，通常用 Lua 脚本完成。\n'
             '\n'
             '```lua\n'
             'if redis.call("get", KEYS[1]) == ARGV[1] then\n'
             '  return redis.call("del", KEYS[1])\n'
             'else\n'
             '  return 0\n'
             'end\n'
             '```\n'
             '\n'
             '**分布式锁不是万能解法**：数据库行级锁更适合保护数据库内的强一致写操作；Redis 锁更适合跨进程协调。涉及资金、库存等关键数据时，应优先让数据库约束和事务兜底。\n'
             '\n'
             '## 8. 异步任务与高并发\n'
             '\n'
             '### 8.1 async/await 的直觉\n'
             '\n'
             'FastAPI 支持异步视图函数。异步的价值主要在 I/O 场景：等待数据库、Redis、外部 HTTP 接口时，事件循环可以去处理其他请求。\n'
             '\n'
             '```text\n'
             '同步阻塞：\n'
             '  请求 A 等数据库 -> 线程闲等\n'
             '\n'
             '异步 I/O：\n'
             '  请求 A 等数据库 -> 事件循环切去处理请求 B\n'
             '```\n'
             '\n'
             '适合 `async` 的任务：\n'
             '\n'
             '- 数据库查询。\n'
             '- Redis 访问。\n'
             '- HTTP API 调用。\n'
             '- 文件、对象存储等 I/O。\n'
             '\n'
             '不适合直接塞进事件循环的任务：\n'
             '\n'
             '- 大量 CPU 计算。\n'
             '- 图像压缩。\n'
             '- 大模型推理。\n'
             '- 长时间同步阻塞库。\n'
             '\n'
             '这些任务应该交给任务队列、进程池或独立服务。\n'
             '\n'
             '### 8.2 后台任务\n'
             '\n'
             'FastAPI 的 `BackgroundTasks` 适合简单、短小、失败影响不大的后台动作，例如发送普通通知。\n'
             '\n'
             '```python\n'
             'from fastapi import BackgroundTasks\n'
             '\n'
             '\n'
             '@router.post("/orders")\n'
             'async def create_order(payload: OrderCreate, tasks: BackgroundTasks):\n'
             '    order = await order_service.create(payload)\n'
             '    tasks.add_task(send_order_email, order.id)\n'
             '    return order\n'
             '```\n'
             '\n'
             '如果任务需要重试、削峰、可观测、失败补偿，应该使用 Celery、RQ、Arq 或消息队列。\n'
             '\n'
             '### 8.3 连接池\n'
             '\n'
             '数据库连接不是越多越好。连接过多会压垮 PostgreSQL，连接过少会让请求排队。\n'
             '\n'
             '基本原则：\n'
             '\n'
             '- 应用侧使用连接池复用连接。\n'
             '- 不要在每个请求里新建数据库引擎。\n'
             '- 慢查询会占用连接，拖慢整个服务。\n'
             '- 长事务会占用锁和连接，应尽量拆短。\n'
             '\n'
             '## 9. 搜索、倒排索引与向量数据库\n'
             '\n'
             "### 9.1 为什么 `LIKE '%keyword%'` 慢\n"
             '\n'
             "普通 B+ 树索引适合前缀、范围、等值查询，但 `LIKE '%keyword%'` 前面有通配符，数据库很难从索引起点定位，只能扫描大量文本。\n"
             '\n'
             '低效查询：\n'
             '\n'
             '```sql\n'
             'SELECT *\n'
             'FROM documents\n'
             "WHERE content LIKE '%搜索引擎%';\n"
             '```\n'
             '\n'
             '**倒排索引**的思路：\n'
             '\n'
             '```text\n'
             '文档 1: 后端 数据库 缓存\n'
             '文档 2: 数据库 索引 搜索\n'
             '\n'
             '倒排索引：\n'
             '后端 -> [文档 1]\n'
             '数据库 -> [文档 1, 文档 2]\n'
             '搜索 -> [文档 2]\n'
             '```\n'
             '\n'
             '搜索引擎会先分词，再通过词到文档的映射快速定位候选文档。Elasticsearch、OpenSearch 等系统都建立在类似思想之上。\n'
             '\n'
             '### 9.2 向量数据库\n'
             '\n'
             '传统数据库擅长精确匹配，向量数据库擅长语义相似度检索。\n'
             '\n'
             '```text\n'
             '文本 / 图片 / 音频\n'
             '  -> Embedding 模型\n'
             '  -> 高维向量\n'
             '  -> 相似度检索\n'
             '  -> 返回语义相近内容\n'
             '```\n'
             '\n'
             '常见相似度：\n'
             '\n'
             '| 方法 | 越大/越小越相似 | 说明 |\n'
             '| --- | --- | --- |\n'
             '| 欧式距离 | 越小 | 直接计算空间距离 |\n'
             '| 内积 | 越大 | 常用于向量检索 |\n'
             '| 余弦相似度 | 越大 | 关注方向相似度 |\n'
             '| BM25 | 越大 | 传统全文检索相关性算法 |\n'
             '\n'
