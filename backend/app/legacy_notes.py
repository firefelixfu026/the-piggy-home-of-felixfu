             '## 3. JavaScript\n'
             '\n'
             '**JavaScript 概述**：JavaScript 是浏览器中的主要脚本语言，负责页面交互、状态管理、DOM '
             '操作、网络请求、数据处理与运行时逻辑控制。现代前端工程中，JavaScript 也承担组件渲染、路由管理、构建工具配置、服务端渲染、跨端开发等职责。\n'
             '\n'
             'JavaScript 的核心地位来自三个方面：\n'
             '\n'
             '| 维度 | 说明 |\n'
             '| --- | --- |\n'
             '| 浏览器原生支持 | 主流浏览器内置 JavaScript 引擎，可直接执行脚本 |\n'
             '| DOM 与 Web API 访问能力 | 可操作页面结构、监听事件、发起网络请求、使用存储与媒体能力 |\n'
             '| 生态规模 | 与 Node.js、npm、前端框架、构建工具、测试工具形成完整工程体系 |\n'
             '\n'
             '### 3.1 JS 语言基础\n'
             '\n'
             '**核心概念梳理**：\n'
             '\n'
             'JavaScript 数据类型分为原始类型与引用类型。原始类型的值通常按值访问，引用类型的变量保存对象引用。\n'
             '\n'
             '| 类型分类 | 具体类型 | 特点 |\n'
             '| --- | --- | --- |\n'
             '| 原始类型 | `string`、`number`、`boolean`、`undefined`、`null`、`symbol`、`bigint` | '
             '值本身不可变，比较时通常比较值 |\n'
             '| 引用类型 | `object`、`array`、`function`、`date`、`regexp` 等 | 变量保存引用地址，比较时通常比较引用 |\n'
             '\n'
             '变量声明方式包括 `var`、`let`、`const`。现代代码中通常优先使用 `const`，需要重新赋值时使用 `let`，避免使用 `var`。\n'
             '\n'
             '| 声明方式 | 作用域 | 是否提升 | 是否可重复声明 | 是否可重新赋值 | 典型用途 |\n'
             '| --- | --- | --- | --- | --- | --- |\n'
             '| `var` | 函数作用域 | 会提升并初始化为 `undefined` | 可以 | 可以 | 旧代码兼容，现代代码不推荐 |\n'
             '| `let` | 块级作用域 | 会提升，但存在暂时性死区 | 不可以 | 可以 | 需要重新赋值的局部变量 |\n'
             '| `const` | 块级作用域 | 会提升，但存在暂时性死区 | 不可以 | 不可以重新绑定 | 常量引用、函数、对象、数组 |\n'
             '\n'
             '作用域用于决定变量的可访问范围。JavaScript 常见作用域包括全局作用域、函数作用域、块级作用域与模块作用域。\n'
             '\n'
             '闭包是函数与其词法作用域环境的组合。当内部函数在外部函数执行结束后仍然持有外部变量引用时，就形成闭包。闭包常用于数据封装、函数工厂、缓存、事件回调等场景。\n'
             '\n'
             '```js\n'
             'function createCounter() {\n'
             '  let count = 0;\n'
             '\n'
             '  return function increment() {\n'
             '    count += 1;\n'
             '    return count;\n'
             '  };\n'
             '}\n'
             '\n'
             'const counter = createCounter();\n'
             '\n'
             'counter(); // 1\n'
             'counter(); // 2\n'
             '```\n'
             '\n'
             '闭包可以保留局部状态，但长期持有不再需要的对象引用可能增加内存占用。因此在事件监听、定时器、缓存结构中需要关注引用释放。\n'
             '\n'
             '### 3.2 ES6 和 ES Next\n'
             '\n'
             '**现代 JS 语法速览**：\n'
             '\n'
             '| 特性名称 | 语法示例 | 解决的核心痛点 |\n'
             '| --- | --- | --- |\n'
             '| 箭头函数 | `const add = (a, b) => a + b;` | 简化函数表达式语法，并固定词法 `this` |\n'
             '| 解构赋值 | `const { id, name } = user;` | 简化对象与数组取值逻辑，减少重复访问路径 |\n'
             '| 模板字符串 | ``const msg = `Hello, ${name}`;`` | 简化字符串拼接，支持多行字符串与表达式插值 |\n'
             '| Promise | `fetch(url).then(res => res.json());` | 统一异步结果抽象，缓解回调嵌套与错误传递混乱 |\n'
             '| `let` / `const` | `const list = []; let index = 0;` | 引入块级作用域，减少 `var` '
             '带来的变量泄漏与提升问题 |\n'
             '| 默认参数 | `function request(url, method = "GET") {}` | 减少函数参数兜底逻辑 |\n'
             '| 剩余参数 | `function sum(...nums) {}` | 以数组形式收集不定数量参数 |\n'
             '| 展开语法 | `const next = [...items, newItem];` | 简化数组、对象复制与参数展开 |\n'
             '| 模块化 | `import { ref } from "vue";` | 提供语言层面的模块导入导出能力 |\n'
             '| 类语法 | `class UserService {}` | 提供更接近传统面向对象语言的构造器与继承语法 |\n'
             '| 可选链 | `const city = user.profile?.address?.city;` | 安全访问深层属性，减少空值判断样板代码 |\n'
             '| 空值合并 | `const size = inputSize ?? 16;` | 仅在 `null` 或 `undefined` 时使用默认值，避免误判 '
             '`0`、`false`、空字符串 |\n'
             '\n'
             'ES Next 通常指已经进入标准流程或已被主流运行时逐步支持的新语法与新 API。工程中使用 ES Next 特性时，需要结合目标浏览器、Node.js '
             '版本、构建工具转译能力与 polyfill 策略综合判断。\n'
             '\n'
             '### 3.3 异步编程\n'
             '\n'
             '**演进历程**：\n'
             '\n'
             'JavaScript '
             '运行环境通常采用事件循环处理异步任务。异步编程模型的演进重点在于降低嵌套复杂度、统一错误处理、提升代码可读性，并更好地表达异步流程之间的依赖关系。\n'
             '\n'
             '| 阶段 | 基本形式 | 解决的问题 | 主要局限 |\n'
             '| --- | --- | --- | --- |\n'
             '| Callback | `readFile(path, (err, data) => {})` | 以函数回调接收异步结果，是早期异步 API 的基础模式 | '
             '多层嵌套容易形成回调地狱，错误处理分散，流程控制困难 |\n'
             '| Promise | `request().then(handle).catch(handleError)` | 将异步结果抽象为状态机，统一成功、失败与链式调用 | '
             '链式调用仍可能较长，复杂条件流程可读性有限 |\n'
             '| Async/Await | `const data = await request();` | 使用接近同步代码的形式描述异步流程，提升顺序依赖逻辑的可读性 | '
             '本质仍基于 Promise，需要理解并发控制、错误捕获与阻塞边界 |\n'
             '\n'
             '回调函数阶段通过约定回调参数传递异步结果。Node.js 早期大量 API 采用 Error First Callback，即第一个参数表示错误，后续参数表示结果。\n'
             '\n'
             '```js\n'
             'readFile("config.json", (error, data) => {\n'
             '  if (error) {\n'
             '    handleError(error);\n'
             '    return;\n'
             '  }\n'
             '\n'
             '  parseConfig(data, (parseError, config) => {\n'
             '    if (parseError) {\n'
             '      handleError(parseError);\n'
             '      return;\n'
             '    }\n'
             '\n'
             '    startApp(config);\n'
             '  });\n'
             '});\n'
             '```\n'
             '\n'
             'Promise 将异步操作封装为 `pending`、`fulfilled`、`rejected` 三种状态，并通过 `then`、`catch`、`finally` '
             '组合后续流程。\n'
             '\n'
             '```js\n'
             'fetch("/api/user")\n'
             '  .then((response) => response.json())\n'
             '  .then((user) => renderUser(user))\n'
             '  .catch((error) => reportError(error))\n'
             '  .finally(() => hideLoading());\n'
             '```\n'
             '\n'
             'Async/Await 是 Promise 之上的语法糖，使异步代码可以使用 `try...catch` 进行集中错误处理。\n'
             '\n'
             '```js\n'
             'async function loadUser() {\n'
             '  try {\n'
             '    showLoading();\n'
             '\n'
             '    const response = await fetch("/api/user");\n'
             '    const user = await response.json();\n'
             '\n'
             '    renderUser(user);\n'
             '  } catch (error) {\n'
             '    reportError(error);\n'
             '  } finally {\n'
             '    hideLoading();\n'
             '  }\n'
             '}\n'
             '```\n'
             '\n'
             '需要并发执行多个互不依赖的异步任务时，直接顺序 `await` 会增加总耗时。此类场景通常使用 '
             '`Promise.all`、`Promise.allSettled`、`Promise.race` 等组合 API。\n'
             '\n'
             '```js\n'
             'const [user, permissions] = await Promise.all([\n'
             '  fetch("/api/user").then((response) => response.json()),\n'
             '  fetch("/api/permissions").then((response) => response.json()),\n'
             ']);\n'
             '```\n'
             '\n'
             '## 4. Node.js 生态与进阶\n'
             '\n'
             '### 4.1 Node.js 基础\n'
             '\n'
             '**运行环境本质**：Node.js 是基于 Chrome V8 引擎构建的 JavaScript 运行时，使 JavaScript '
             '可以脱离浏览器在服务器、命令行工具、构建系统、桌面应用后端等环境中执行。\n'
             '\n'
             'Node.js 的核心特性包括：\n'
             '\n'
             '| 特性 | 说明 |\n'
             '| --- | --- |\n'
             '| V8 引擎 | 负责解析、编译与执行 JavaScript，提供高性能 JIT 编译能力 |\n'
             '| 事件驱动 | 通过事件循环调度异步任务，适合处理大量 I/O 事件 |\n'
             '| 非阻塞 I/O | 文件、网络、数据库等 I/O 操作通常不会阻塞主线程 |\n'
             '| 单线程主模型 | JavaScript 代码主要在单个事件循环线程中执行，降低共享状态并发复杂度 |\n'
             '| libuv | 提供跨平台异步 I/O、线程池、事件循环等底层能力 |\n'
             '| npm 生态 | 提供庞大的包管理与模块复用体系 |\n'
             '\n'
             'Node.js 适合 I/O 密集型服务，例如 API 服务、BFF 层、实时通信、构建工具、CLI 工具等。CPU 密集型任务需要结合 Worker '
             'Threads、子进程、任务队列或独立计算服务进行拆分。\n'
             '\n'
             '### 4.2 进阶框架和库\n'
             '\n'
             '**Sequelize**：Sequelize 是 Node.js 生态中的 ORM（Object Relational Mapping，对象关系映射）框架，用于在 '
             'JavaScript 对象模型与关系型数据库表结构之间建立映射关系。它支持 MySQL、PostgreSQL、SQLite、MariaDB、Microsoft SQL '
             'Server 等数据库。\n'
             '\n'
             'Sequelize 的核心机制包括：\n'
             '\n'
             '| 机制 | 说明 |\n'
             '| --- | --- |\n'
             '| Model | 使用 JavaScript 类或定义对象描述数据库表结构 |\n'
             '| DataTypes | 描述字段类型、是否允许为空、默认值、校验规则等 |\n'
             '| Association | 描述表之间的一对一、一对多、多对多关系 |\n'
             '| Migration | 管理数据库结构变更，便于团队协作与版本追踪 |\n'
             '| Query Builder | 通过对象配置生成 SQL，减少手写 SQL 的重复工作 |\n'
             '| Transaction | 支持事务控制，保证多步骤数据库操作的一致性 |\n'
             '\n'
             '```js\n'
             'import { DataTypes } from "sequelize";\n'
             'import { sequelize } from "./database.js";\n'
             '\n'
             'const User = sequelize.define("User", {\n'
             '  id: {\n'
             '    type: DataTypes.INTEGER,\n'
             '    primaryKey: true,\n'
             '    autoIncrement: true,\n'
             '  },\n'
             '  name: {\n'
             '    type: DataTypes.STRING,\n'
             '    allowNull: false,\n'
             '  },\n'
             '  email: {\n'
             '    type: DataTypes.STRING,\n'
             '    allowNull: false,\n'
             '    unique: true,\n'
             '  },\n'
             '});\n'
             '\n'
             'const users = await User.findAll({\n'
             '  where: {\n'
             '    name: "Felix",\n'
             '  },\n'
             '});\n'
             '```\n'
             '\n'
             'ORM 可以提升 CRUD 开发效率与类型抽象能力，但复杂查询、性能调优、索引策略、事务边界仍需要理解底层 SQL 与数据库执行计划。\n'
             '\n'
             '**Axios**：Axios 是基于 Promise 的 HTTP 客户端，可同时运行在浏览器与 Node.js '
             '环境中。它封装了请求配置、响应处理、错误处理、拦截器、取消请求、超时控制等常用能力。\n'
             '\n'
             'Axios 的核心优势包括：\n'
             '\n'
             '| 能力 | 说明 |\n'
             '| --- | --- |\n'
             '| 请求与响应拦截器 | 可统一注入 Token、处理错误码、刷新登录态、记录日志 |\n'
             '| 自动 JSON 转换 | 默认对 JSON 请求与响应进行序列化和反序列化处理 |\n'
             '| 超时配置 | 可通过 `timeout` 控制请求最大等待时间 |\n'
             '| 统一错误对象 | 对 HTTP 错误、网络错误、配置错误进行统一封装 |\n'
             '| 实例化配置 | 可创建多个 HTTP 实例，隔离不同服务的 baseURL、headers、拦截器 |\n'
             '| 浏览器与 Node.js 双端支持 | 浏览器使用 XHR 或 Fetch 适配，Node.js 使用 HTTP 相关能力 |\n'
             '\n'
             'Axios 与原生 Fetch API 对比如下：\n'
             '\n'
             '| 对比维度 | Axios | Fetch API |\n'
             '| --- | --- | --- |\n'
             '| API 来源 | 第三方库 | 浏览器原生 Web API |\n'
             '| Promise 支持 | 原生基于 Promise | 原生基于 Promise |\n'
             '| JSON 处理 | 默认自动转换 JSON 数据 | 需要显式调用 `response.json()` |\n'
             '| HTTP 错误处理 | 非 2xx 状态通常进入 rejected 流程 | 非 2xx 状态不会自动 reject，需要检查 `response.ok` |\n'
             '| 拦截器 | 内置请求与响应拦截器 | 原生不提供，需要自行封装 |\n'
             '| 超时控制 | 内置 `timeout` 配置 | 通常结合 `AbortController` 实现 |\n'
             '| 包体积 | 增加额外依赖 | 无额外依赖 |\n'
             '\n'
             '```js\n'
             'import axios from "axios";\n'
             '\n'
             'const http = axios.create({\n'
             '  baseURL: "/api",\n'
             '  timeout: 8000,\n'
             '});\n'
             '\n'
             'http.interceptors.request.use((config) => {\n'
             '  config.headers.Authorization = `Bearer ${getToken()}`;\n'
             '  return config;\n'
             '});\n'
             '\n'
             'http.interceptors.response.use(\n'
             '  (response) => response.data,\n'
             '  (error) => Promise.reject(normalizeHttpError(error)),\n'
             ');\n'
             '\n'
             'const user = await http.get("/user");\n'
             '```\n'
             '\n'
             '**Mock.js**：Mock.js 是用于生成模拟数据与拦截 Ajax 请求的前端开发工具。它通过数据模板语法生成随机数据，并可在开发阶段拦截指定 URL '
             '与请求方法，使前端在后端接口未完成或接口不稳定时独立推进页面开发。\n'
             '\n'
             'Mock.js 的典型作用包括：\n'
             '\n'
             '| 场景 | 说明 |\n'
             '| --- | --- |\n'
             '| 接口未完成 | 使用模拟接口返回结构化数据，前端页面可提前联调 UI 与状态 |\n'
             '| 批量造数 | 使用模板快速生成列表、用户、订单、图表等测试数据 |\n'
             '| 边界测试 | 模拟空列表、异常状态、超长文本、分页结果等情况 |\n'
             '| 解耦开发 | 降低前后端开发进度互相阻塞的风险 |\n'
             '\n'
